import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { supabase } from "@/integrations/supabase/client";
import { toast } from "sonner";

export interface GalleryImage {
  id: string;
  image_url: string;
  alt_text: string | null;
  sort_order: number;
  created_at: string;
  updated_at: string;
}

export const useGallery = () => {
  const queryClient = useQueryClient();

  const { data: images, isLoading } = useQuery({
    queryKey: ["gallery-images"],
    queryFn: async () => {
      const { data, error } = await supabase
        .from("gallery_images")
        .select("*")
        .order("sort_order", { ascending: true });

      if (error) throw error;
      return data as GalleryImage[];
    },
  });

  const uploadImage = async (file: File): Promise<string> => {
    const fileExt = file.name.split(".").pop();
    const fileName = `${crypto.randomUUID()}.${fileExt}`;
    const filePath = `gallery/${fileName}`;

    const { error: uploadError } = await supabase.storage
      .from("branding")
      .upload(filePath, file);

    if (uploadError) throw uploadError;

    const { data } = supabase.storage.from("branding").getPublicUrl(filePath);
    return data.publicUrl;
  };

  const addImage = useMutation({
    mutationFn: async ({ file, altText }: { file: File; altText?: string }) => {
      const imageUrl = await uploadImage(file);
      
      // Get max sort_order
      const { data: existing } = await supabase
        .from("gallery_images")
        .select("sort_order")
        .order("sort_order", { ascending: false })
        .limit(1);

      const nextOrder = existing && existing.length > 0 ? existing[0].sort_order + 1 : 1;

      const { error } = await supabase.from("gallery_images").insert({
        image_url: imageUrl,
        alt_text: altText || null,
        sort_order: nextOrder,
      });

      if (error) throw error;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["gallery-images"] });
      toast.success("Bild tillagd!");
    },
    onError: (error) => {
      toast.error("Kunde inte lÃ¤gga till bild: " + error.message);
    },
  });

  const updateImage = useMutation({
    mutationFn: async ({
      id,
      file,
      altText,
    }: {
      id: string;
      file?: File;
      altText?: string;
    }) => {
      let imageUrl: string | undefined;
      
      if (file) {
        imageUrl = await uploadImage(file);
      }

      const updateData: Partial<GalleryImage> = {};
      if (imageUrl) updateData.image_url = imageUrl;
      if (altText !== undefined) updateData.alt_text = altText;

      const { error } = await supabase
        .from("gallery_images")
        .update(updateData)
        .eq("id", id);

      if (error) throw error;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["gallery-images"] });
      toast.success("Bild uppdaterad!");
    },
    onError: (error) => {
      toast.error("Kunde inte uppdatera bild: " + error.message);
    },
  });

  const deleteImage = useMutation({
    mutationFn: async (id: string) => {
      const { error } = await supabase
        .from("gallery_images")
        .delete()
        .eq("id", id);

      if (error) throw error;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["gallery-images"] });
      toast.success("Bild borttagen!");
    },
    onError: (error) => {
      toast.error("Kunde inte ta bort bild: " + error.message);
    },
  });

  return {
    images: images || [],
    isLoading,
    addImage,
    updateImage,
    deleteImage,
  };
};
